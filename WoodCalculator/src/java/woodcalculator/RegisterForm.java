    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
        String fname = jTextField_fullname.getText();
        String uname = jTextField_username.getText();
        String pass1 = String.valueOf(jPasswordField_password.getPassword());
        String pass2 = String.valueOf(jPasswordField_confirmpassword.getPassword());
        String tel = jTextField_telefon.getText();
        
        if(VerifyFields()){
            if(!CheckUsername(uname)){
                PreparedStatement ps;
                ResultSet rs;
                String registerUserQuery = "INSERT INTO `users`(`full_name`, `username`, `password`, `phone`) VALUES (?,?,?,?)";
                
                try{
                    
                    ps = My_CNX.getConnection().prepareStatement(registerUserQuery);
                    ps.setString(1, fname);
                    ps.setString(2, uname);
                    ps.setString(3, pass1);
                    ps.setString(4, tel);
                    try{
                        if(ps.executeUpdate() != 0){
                            JOptionPane.showMessageDialog(null, "Contul dumneavoastra a fost creat!");
                            LoginForm login = new LoginForm();
                            login.setVisible(true);
                            login.pack();
                            login.setLocationRelativeTo(null);
                            this.dispose();
                        }
                        else{
                             JOptionPane.showMessageDialog(null, "Error! Check Your Information!");
                        }
                    }
                    catch(SQLException ex){
                        Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE,null,ex);
                    }
                }
                catch(SQLException ex){
                     Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE,null,ex);
                     }
                
            }
        }
    }//GEN-LAST:event_jButton_registerActionPerformed

