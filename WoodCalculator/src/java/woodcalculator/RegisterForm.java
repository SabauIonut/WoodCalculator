    //functie care verifica daca toate campurile sunt completate
    public boolean VerifyFields()
    {
        String fname = jTextField_fullname.getText();
        String uname = jTextField_username.getText();
        String pass1 = String.valueOf(jPasswordField_password.getPassword());
        String pass2 = String.valueOf(jPasswordField_confirmpassword.getPassword());
        String tel = jTextField_telefon.getText();
        //verifica daca parolele sunt identice
        if(fname.trim().equals("") || uname.trim().equals("") || pass1.trim().equals("") || pass2.trim().equals("") || tel.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Unul sau mai multe câmpuri sunt goale!","Empty Fields",2);
            return false;
        }
        else{
            if(!pass1.equals(pass2)){
                 JOptionPane.showMessageDialog(null, "Parolele nu se potrivesc!","Confirm Password",2);
                 return false;
                }
            else{
                return true;
            }
        }
    }
    
    //fuctie care verifica daca username se afla in baza de date
    public boolean CheckUsername(String username){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exist = false;
        String query = "SELECT * FROM `users` WHERE `username` = ?";
        
        try{
            st = My_CNX.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            if(rs.next()){
                username_exist = true;
                JOptionPane.showMessageDialog(null, " Utilizator existent! Vă rugăm completați alt nume de utilizator", "Username Failed", 2);
            }
        }
        catch(SQLException ex){
            Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE,null,ex);
        }
                
        return username_exist;
        
    }
